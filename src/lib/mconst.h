#ifndef H_MCONST_H
#define H_MCONST_H

#include <math.h>
#ifdef USE_GSL
#  include <gsl/gsl_math.h>
#endif

#ifndef M_PI
#  define M_PI        3.14159265358979323846f  /* pi */
#endif
#ifndef M_PI_2
#  define M_PI_2      1.57079632679489661923f  /* pi / 2 */
#endif
#ifndef M_2PI
#  define M_2PI       6.28318530717958647692f  /* 2 * pi */
#endif
#ifndef M_180_PI
#  define M_180_PI    57.2957795130823208768f  /* 180 / pi */
#endif
#ifndef M_PI_180
#  define M_PI_180    .017453292519943295769f  /* pi / 180 */
#endif

#endif
